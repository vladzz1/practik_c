class Car
{
	string mark;
	float volume;
	int year;
	int mileage;
protected:
	int speed;
public:
	Car() :mark("no data"), volume(0), year(0), mileage(0), speed(0) {}
	Car(string mark, float volume, int year, int mileage) :mark(mark), volume(volume), year(year), mileage(mileage), speed(0) {}
	void printCar()const
	{
		cout << "-----------------------car-----------------------\n";
		cout << "mark: " << mark << endl;
		cout << "volume: " << volume << endl;
		cout << "year: " << year << endl;
		cout << "mileage: " << mileage << endl;
	}
	virtual void drive()const
	{
		cout << "i am a car !!!! i car drive with speed " << speed << "km/h" << endl;
	}
	void setSpeed(int speed)
	{
		if (speed > 0)
		{
			this->speed = speed;
		}
	}
};

class PoliceCar : public Car
{
	float soundVolume;
public:
	PoliceCar() :soundVolume(0), Car() {}
	PoliceCar(string mark, float volume, int year, int mileage, float soundVolume) :soundVolume(soundVolume), Car(mark, volume, year, mileage) {}
	void printCar()const
	{
		cout << "-----------------------police car-----------------------\n";
		Car::printCar();
	}
	void drive()const override
	{
		cout << "i am a police car !!!! i car drive with speed " << speed << "km/h" << endl;
	}
	void beepBeep()
	{
		cout << "Viyviyviyviyviy\n";
		Beep(1000, 500);
		Beep(523, 500);
		Beep(1000, 500);
		Beep(523, 500);
	}
};

class SportCar : public Car
{
	float turbo;
public:
	SportCar() :turbo(0), Car() {}
	SportCar(string mark, float volume, int year, int mileage, float turbo) :turbo(turbo), Car(mark, volume, year, mileage) {}
public:
	void printCar()const
	{
		cout << "-----------------------sport car-----------------------\n";
		Car::printCar();
	}
	void drive()const override
	{
		cout << "i am a sport car !!!! i car drive with speed " << speed << "km/h" << endl;
	}
	void nitro()
	{
		speed += turbo;
		cout << "turbo speed " << speed << " km/h" << endl;
	}
};

